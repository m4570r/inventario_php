todo el proceso relacionado con las devoluciones, desde la 
solicitud inicial hasta la resolución final. Este controlador 
permite a la empresa ofrecer un servicio al cliente eficiente 
y resolver problemas relacionados con productos defectuosos, 
errores de envío u otras situaciones que requieran una devolución.

Funcionalidades Principales:
1. Registro de Devoluciones: Permite a los usuarios, generalmente 
clientes, iniciar solicitudes de devolución proporcionando detalles 
como el motivo de la devolución, número de pedido y descripción 
del problema.

